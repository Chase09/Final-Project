import greenfoot.*;  // (World, Actor, GreenfootImage, Greenfoot and MouseInfo)

public class TurnBox3 extends Actor
{
    /**
     * This is the constructor that sets the image of the TurnBox3 to a black box that 
     * causes the BarrelsSpinning to rotate to the other direction.
     * 
     * @param There are no parameters
     * @return Nothing is returned
     */
    public TurnBox3()
    {
        GreenfootImage image = new GreenfootImage("TurnBox.png");
        setImage(image);
    } 
}
